void red_read(void) // function to select red filter and display the color by glowing red LED. The pulse will be more if the color is red. The pulse will be very less if its blue or green.
{
	//Red
	filter_red(); //select red filter
	pulse=0; //reset the count to 0
	_delay_ms(100); //capture the pulses for 100 ms or 0.1 second
	red = pulse;  //store the count in variable called red
}

