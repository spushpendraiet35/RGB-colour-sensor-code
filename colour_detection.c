//Function to configure LCD port
void lcd_port_config (void)
{
	DDRC = DDRC | 0xF7; //all the LCD pin's direction set as output
	PORTC = PORTC & 0x80; // all the LCD pins are set to logic 0 except PORTC 7
}

//ADC pin configuration
void adc_pin_config (void)
{
	DDRF = 0x00; //set PORTF direction as input
	PORTF = 0x00; //set PORTF pins floating
	DDRK = 0x00; //set PORTK direction as input
	PORTK = 0x00; //set PORTK pins floating
}

//Function to configure ports to enable robot's motion
void motion_pin_config (void)
{
	DDRB = 0x0F;
	PORTB = 0xF0;
	DDRL = 0x18;   //Setting PL3 and PL4 pins as output for PWM generation
	PORTL = 0x18; //PL3 and PL4 pins are for velocity control using PWM.
}

void color_sensor_pin_config(void)
{
	DDRD  = DDRD | 0xFE; //set PD0 as input for color sensor output
	PORTD = PORTD | 0x01; //Enable internal pull-up for PORTD 0 pin
	/*****************************************
	Define DDR and PORT values for the port on which Color sensor is connected
	******************************************/
}

//Function to initialize Buzzer
void buzzer_pin_config (void)
{
	DDRE = DDRE | 0x08;		//Setting PORTC 3 as output
	PORTE = PORTE & 0xF7;		//Setting PORTC 3 logic low to turnoff buzzer
}

void color_sensor_pin_interrupt_init(void) //Interrupt 0 enable
{
	cli(); //Clears the global interrupt
	EICRA = EICRA | 0x02; // INT0 is set to trigger with falling edge
	EIMSK = EIMSK | 0x01; // Enable Interrupt INT0 for color sensor
	sei(); // Enables the global interrupt
}

void rgb_port_config (void)
{
	DDRA = 0x07; //setting all the LED pin's direction set as output
	PORTA = 0x07; //setting all the LED pins are set to logic 1
	/*****************************************
	Define DDR and PORT values for the port on which RGB LED is connected
	******************************************/
}

